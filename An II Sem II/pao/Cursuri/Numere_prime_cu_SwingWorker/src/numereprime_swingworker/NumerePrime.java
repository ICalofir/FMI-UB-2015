import java.util.List;
import java.util.concurrent.ExecutionException;
import javax.swing.JOptionPane;
import javax.swing.JTextArea;
import javax.swing.SwingWorker;

class GeneratorNumerePrime extends SwingWorker<Integer , Integer> 
{
    int max;
    JTextArea nrPrime;

    public GeneratorNumerePrime(int max, JTextArea nrPrime)
    {
        this.max = max;
        this.nrPrime = nrPrime;
    }
    
    //aici se implementeaza "firul de executare"
    protected Integer doInBackground()
    {
        int d , i , k;
        
        k = 0;
        for(i = 2; (i <= max) && (isCancelled() == false); i++)
        {
            boolean prim = true;
            for(d = 2; (d <= i/2) && (prim == true); d++)
                if(i % d == 0)
                    prim = false;
            
            if(prim == true)
            {
                //"se publica" un numar prim
                publish(i);
                k++;
            }
        }
        
        //numarul de nr. prime generate
        return k;
    }
    
    //in lista se gasesc toate numerele "publicate"
    protected void process(List<Integer> prime) 
    {
        for(Integer p : prime)
            nrPrime.append(p + "\n");
    }
    
    public void done()
    {
        try 
        {
            if(isCancelled() == false)
                JOptionPane.showMessageDialog(null , "Au fost generate " + get() + " de numere prime!" , "Numere prime", JOptionPane.INFORMATION_MESSAGE);
            else
                JOptionPane.showMessageDialog(null , "Generatorul de numere prime a fost intrerupt!" , "Numere prime", JOptionPane.INFORMATION_MESSAGE);
        } 
        catch (InterruptedException ex) 
        {
            JOptionPane.showMessageDialog(null , "Generatorul de numere prime a fost intrerupt!" , "Numere prime", JOptionPane.INFORMATION_MESSAGE);
        } 
        catch (ExecutionException ex) {
            JOptionPane.showMessageDialog(null , "A aparut o eroare in generator!" , "Numere prime", JOptionPane.INFORMATION_MESSAGE);
        }
   }
}

